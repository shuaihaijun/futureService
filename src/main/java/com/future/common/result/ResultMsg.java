package com.future.common.result;

public class ResultMsg {

    private int code;
    private String msg;

    public int getCode(){
        return code;
    }
    public void setCode(int dataCode){
        code=dataCode;
    }

    public String getMsg(){
        return msg;
    }

    public void setMsg(String dataMsg){
        msg=dataMsg;
    }


}
